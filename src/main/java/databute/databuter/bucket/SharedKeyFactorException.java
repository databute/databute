package databute.databuter.bucket;

public class SharedKeyFactorException extends Exception {
    public SharedKeyFactorException() {
        super();
    }

    public SharedKeyFactorException(String message) {
        super(message);
    }

    public SharedKeyFactorException(String message, Throwable cause) {
        super(message, cause);
    }

    public SharedKeyFactorException(Throwable cause) {
        super(cause);
    }
}
